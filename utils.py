def format_size(size: int) -> str:
    """Преобразует размер в байтах в читаемую строку с единицей измерения.

    Последовательно делит значение на 1024, пока результат не окажется
    меньше 1024, и возвращает строку с подходящим суффиксом.
    Поддерживаемые единицы: B, KB, MB, GB, TB, PB.

    Args:
        size (int): Размер в байтах. Должен быть неотрицательным целым числом.

    Returns:
        str: Отформатированная строка вида ``"3.7 MB"`` или ``"120.0 KB"``.
    """
    for unit in ('B', 'KB', 'MB', 'GB', 'TB'):
        if size < 1024:
            return f"{size:.1f} {unit}"
        size /= 1024
    return f"{size:.1f} PB"


